namespace OBeautifulCode.CodeGen.ModelObject
{
    using System.Collections.Generic;
    using System.Linq;

    using OBeautifulCode.Assertion.Recipes;
    using OBeautifulCode.Collection.Recipes;
    using OBeautifulCode.Type.Recipes;

    using static System.FormattableString;

    /// <summary>
    /// Generates model implementation code.
    /// </summary>
    internal static class ModelImplementationGeneration
    {
        /// <summary>
        /// Generates code that implements standard features of a model, including
        /// equality checks, hash code generation, cloning methods, and a friendly ToString().
        /// </summary>
        /// <param name="modelType">The type of model.</param>
        /// <returns>
        /// Generated code that implements the standard features for the specified model type.
        /// </returns>
        public static string GenerateCodeForModelImplementation(
            this ModelType modelType)
        {
            new { modelType }.AsArg().Must().NotBeNull();

            var items = new List<string>
            {
                "// --------------------------------------------------------------------------------------------------------------------",
                "// <auto-generated>",
                Invariant($"//   Generated using {GenerationShared.GetCodeGenAssemblyName()} ({GenerationShared.GetCodeGenAssemblyVersion()})"),
                "// </auto-generated>",
                "// --------------------------------------------------------------------------------------------------------------------",
                string.Empty,
                Invariant($"namespace {modelType.TypeNamespace}"),
                "{",
                "    using System;",
                "    using System.CodeDom.Compiler;",
                "    using System.Collections.Concurrent;",
                "    using System.Collections.Generic;",
                "    using System.Collections.ObjectModel;",
                "    using System.Diagnostics.CodeAnalysis;",
                "    using System.Globalization;",
                "    using System.Linq;",
                string.Empty,
                "    using OBeautifulCode.Equality.Recipes;",
                "    using OBeautifulCode.Type;",
                string.Empty,
                "    using static System.FormattableString;",
                string.Empty,
                "    [ExcludeFromCodeCoverage]",
                Invariant($"    [GeneratedCode(\"{GenerationShared.GetCodeGenAssemblyName()}\", \"{GenerationShared.GetCodeGenAssemblyVersion()}\")]"),
                Invariant($"    public partial class {modelType.TypeReadableString} : {modelType.RequiredInterfaces.Select(_ => _.ToStringCompilable()).ToDelimitedString(", ")}"),
                "    {",
            };

            var firstNewlineInsideClassIndex = items.Count;

            if (modelType.RequiresEquality)
            {
                items.Add(string.Empty);
                items.Add("    " + modelType.GenerateEqualityMethods());
            }

            if (modelType.RequiresHashing)
            {
                var hashingMethods = modelType.GenerateHashingMethods();

                if (hashingMethods != null)
                {
                    items.Add(string.Empty);
                    items.Add("    " + hashingMethods);
                }
            }

            if (modelType.RequiresDeepCloning)
            {
                items.Add(string.Empty);
                items.Add("    " + modelType.GenerateCloningMethods());
            }

            if (modelType.RequiresStringRepresentation)
            {
                var stringRepresentationMethods = modelType.GenerateStringRepresentationMethods();

                if (stringRepresentationMethods != null)
                {
                    items.Add(string.Empty);
                    items.Add("    " + stringRepresentationMethods);
                }
            }

            items.Add("    }");
            items.Add("}");

            if (items[firstNewlineInsideClassIndex] == string.Empty)
            {
                items.RemoveAt(firstNewlineInsideClassIndex);
            }

            var result = items.Where(_ => _ != null).ToNewLineDelimited();

            return result;
        }
    }
}